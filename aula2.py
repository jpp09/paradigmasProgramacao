# Declarando um vetor
minha_lista = []

#Adicionando valores na lista
for i in range(0,10):
    minha_lista.append(i)

#Impressão da lista completa
print(minha_lista)

#Impressão do primeiro index
print(minha_lista[::-1])

#Impressão do último index 
print(minha_lista[-1])

#Impressão da lista ao contrario
print(minha_lista[::-1])


